namespace House
{
    /// <summary>
    /// Тип параметра модели
    /// </summary>
    public enum ParameterType
    {
        /// <summary>
        /// Ширина дома (1)
        /// </summary>
        HouseWidth,

        /// <summary>
        /// Длина дома (2)
        /// </summary>
        HouseLength,

        /// <summary>
        /// Количество этажей (3)
        /// </summary>
        FloorsCount,

        /// <summary>
        /// Высота окна (4)
        /// </summary>
        WindowHeight,

        /// <summary>
        /// Ширина окна (5)
        /// </summary>
        WindowWidth,

        /// <summary>
        /// Расстояние между окнами по горизонтали (6)
        /// </summary>
        WindowDistanceHor,

        /// <summary>
        /// Расстояние между окнами по вертикали (7)
        /// </summary>
        WindowDistanceVer,

        /// <summary>
        /// Высота балкона (8)
        /// </summary>
        BalconHeight,

        /// <summary>
        /// Ширина балкона (9)
        /// </summary>
        BalconWidth,

        /// <summary>
        /// Длина балкона (10)
        /// </summary>
        BalconLength,

        /// <summary>
        /// Высота двери (11)
        /// </summary>
        DoorHeight,

        /// <summary>
        /// Ширина двери (12)
        /// </summary>
        DoorWidth,

        /// <summary>
        /// Длина козырька (13)
        /// </summary>
        PeakLength,

        /// <summary>
        /// Высота арки (14)
        /// </summary>
        ArcHeight,

        /// <summary>
        /// Ширина арки (15)
        /// </summary>
        ArcWidth,

        /// <summary>
        /// Начало арки (16)
        /// </summary>
        StartPoint
    }
}
